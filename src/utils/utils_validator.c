#include "minishell.h"

int	iswhitespace(int ch)
{
	return (ch == ' ' || ch == '\n' || ch == '\t'
		|| ch == '\f' || ch == '\v' || ch == '\r');
}

int	isspecialsym(int ch)
{
	return (ch == '|' || ch == '>' || ch == '<');
}
